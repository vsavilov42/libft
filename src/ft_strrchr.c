#include "../libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*a;

	a = 0;
	while (*s)
	{
		if (*s == c)
			a = (char *)s;
		++s;
	}
	if (a)
		return (a);
	if (c == '\0')
		return ((char *) s);
	return (0);
}
